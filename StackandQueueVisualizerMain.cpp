// Helper enum and function used to display build information in the "About" dialog.
enum wxbuildinfoformat {
    short_f, long_f };

// Returns a string describing the wxWidgets build.
// If "long_f" is passed, extra details such as OS and Unicode/ANSI are appended.
wxString wxbuildinfo(wxbuildinfoformat format)
{
    wxString wxbuild(wxVERSION_STRING);   // Base version string, e.g. "wxWidgets 3.1.7"

    if (format == long_f )
    {
#if defined(__WXMSW__)
        wxbuild << _T("-Windows");        // Application is built for Windows
#elif defined(__UNIX__)
        wxbuild << _T("-Linux");          // Application is built for Linux
#endif

#if wxUSE_UNICODE
        wxbuild << _T("-Unicode build");  // Using Unicode build of wxWidgets
#else
        wxbuild << _T("-ANSI build");     // Using ANSI build of wxWidgets
#endif // wxUSE_UNICODE
    }

    return wxbuild;
}

