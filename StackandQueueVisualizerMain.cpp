//Event Handler for Push button click event
void StackandQueueVisualizerFrame::OnpushButtonClick(wxCommandEvent& event)
{
    long val; //member variable of long type

    //if the input text cannot be converted to a number print an error message
    //Dereference TextCtrl1 pointer and use chain calling to access GetValue and ToLong methods
    if(!TextCtrl1->GetValue().ToLong(&val)){
        wxMessageBox("Input Error!","Please enter a valid integer", wxOK | wxICON_ERROR, this);
        return;
    }

    m_stack.push_back((int)val); //Add input to the top of of the stack by calling the push_back method
    RefreshStackDisplay(); //Call RefreshStackDisplay function to show new Stack
    TextCtrl1->Clear(); //Clear input text box
}

//Event Handler for Pop button click event
void StackandQueueVisualizerFrame::OnpopButtonClick(wxCommandEvent& event)
{
    //if the stack is empty we cannot pop so display error message
    //Call empty method on the m_stack object
    if (m_stack.empty()){
        wxMessageBox("The stack is empty.","Oops!", wxOK | wxICON_INFORMATION, this);
        return;
    }

    m_stack.pop_back(); //Remove the element at the top of the stack (LIFO)
    RefreshStackDisplay(); //Call RefreshStackDisplay function to show new Stack

}

//Event Handler for Enqueue button click event
void StackandQueueVisualizerFrame::OnqueueButtonClick(wxCommandEvent& event)
{
    long val; //member variable of long type

    //if the input text cannot be converted to a number print an error message
    //Dereference TextCtrl2 pointer and use chain calling to access GetValue and ToLong methods
    if(!TextCtrl2->GetValue().ToLong(&val)){
        wxMessageBox("Input Error!","Please enter a valid integer", wxOK | wxICON_ERROR, this);
        return;
    }

    m_queue.push_back((int)val); //Add input to the back of of the queue by calling the push method
    RefreshQueueDisplay(); //Call RefreshQueueDisplay function to show new Queue
    TextCtrl2->Clear(); //Clear input text box
}

//Event Handler for Dequeue button click event
void StackandQueueVisualizerFrame::OndequeueButtonClick(wxCommandEvent& event)
{
    //if the queue is empty we cannot remove an element so display error message
    //Call empty method on the m_queue object
    if (m_queue.empty()){
        wxMessageBox("The queue is empty.","Oops!", wxOK | wxICON_INFORMATION, this);
        return;
    }

    m_queue.pop_front(); //Remove the element at the front of the queue (FIFO)
    RefreshQueueDisplay(); //Call RefreshQueueDisplay function to show new Queue

}

//--------------------------------------------------------------//
//                 Visual Display Function definitions
//--------------------------------------------------------------//

void StackandQueueVisualizerFrame::RefreshStackDisplay(){
    StackList->Clear(); //clear previous display by dereferencing and calling the Clear function

    //for loop to display contents of the stack
    //decrementing for loop to show the last element of the stack at the top of the list
    //could have also used a for_each algorithm with a reverse iterator and an append function to achieve the same results
    for(int i=(int)m_stack.size()-1; i>=0; --i){
        //Deference StackList pointer to access the append function
        // Add a formatted String with the stack element to the list
        StackList->Append(wxString::Format("%d", m_stack[i]));
    }
}

void StackandQueueVisualizerFrame::RefreshQueueDisplay(){
    QueueList->Clear(); //clear previous display by dereferencing and calling the Clear function

    //for loop to display contents of the queue
    //incrementing for loop to show the first element in the queue at the top of the list
    //could have also used a for_each algorithm with a forward iterator and an append function to achieve the same results
    for (int i=0; i < m_queue.size(); ++i){
        //Deference QueueList pointer to access the append function
        // Add a formatted String with the queue element to the list
        QueueList->Append(wxString::Format("%d", m_queue[i]));
    }
}
